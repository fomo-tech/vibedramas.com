#!/usr/bin/env node
/**
 * Tự động tăng BUILD_VERSION trong public/sw.js trước mỗi lần build.
 * Chạy qua "prebuild" script trong package.json.
 */

const fs = require("fs");
const path = require("path");

const swPath = path.join(__dirname, "../public/sw.js");
const buildInfoPath = path.join(__dirname, "../src/constants/buildInfo.ts");
const content = fs.readFileSync(swPath, "utf8");

const match = content.match(/const BUILD_VERSION = "(\d+)\.(\d+)\.(\d+)"/);
if (!match) {
  console.error("[bump-sw] ❌ Không tìm thấy BUILD_VERSION trong sw.js");
  process.exit(1);
}

const [, major, minor, patch] = match;
const newPatch = parseInt(patch, 10) + 1;
const newVersion = `${major}.${minor}.${newPatch}`;

const updated = content.replace(
  /const BUILD_VERSION = "\d+\.\d+\.\d+"/,
  `const BUILD_VERSION = "${newVersion}"`,
);

fs.writeFileSync(swPath, updated, "utf8");

// Keep a TS constant in sync so UI can display build version.
const buildInfoContent =
  `// Auto-generated by scripts/bump-sw-version.js\n` +
  `export const BUILD_VERSION = "${newVersion}";\n`;
fs.writeFileSync(buildInfoPath, buildInfoContent, "utf8");

console.log(
  `[bump-sw] ✅ BUILD_VERSION: ${major}.${minor}.${patch} → ${newVersion}`,
);
